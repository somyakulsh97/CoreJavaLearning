public class Lab040 {
    public static void main(String[] args) {

        int a = 10;
        System.out.println(++a);
        // pre increment means : do increment first then print.


        int a1 = 10;
        System.out.println(a++);
        System.out.println(a1);
//        post increment means : first print the value then print.

    }
}
